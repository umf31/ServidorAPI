using ServidorAPI.Dominio.Servicios.Base;

namespace ServidorAPI.Dominio.Entidades.Servidor
{
    public class Colonia : EntidadBase
    {
        public int CodigoPostal { get; set; }
        public int AsentamientoId { get; set; }
        public int MunicipioId { get; set; }
        public decimal? Latitud { get; set; }
        public decimal? Longitud { get; set; }
        public string? Geolocalizacion { get; set; } = null!;
        public string? Descripcion { get; set; }
        public string? Imagen { get; set; }
        public virtual Asentamiento Asentamiento { get; set; } = null!;
        public virtual Municipio Municipio { get; set; } = null!;
        public virtual Status Status { get; set; } = null!;
        public virtual ICollection<Empleado> Empleados { get; set; } = null!;
        public virtual ICollection<Unidad> Unidades { get; set; } = null!;
    }
}