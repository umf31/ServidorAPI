namespace ServidorAPI.Dominio.Servicios.Sadim
{
    public partial class CoberturaPrevenIMSS
    {
        public decimal? total;
        public string CvePresup { get; set; } = null!;
        public string Periodo { get; set; } = null!;
        public decimal? DhAip { get; set; }
        public decimal? TotalPob { get; set; }
        public decimal? CobAip { get; set; }
        public decimal? Total { get { total = ObtenerIndicador(); return total; } }
        private decimal ObtenerIndicador()
        {
            int multiplicador = 100;
            decimal numerador = Convert.ToDecimal(DhAip);
            decimal denominador = Convert.ToDecimal(TotalPob);
            decimal total = numerador / denominador * multiplicador;
            return total;
        }
    }
}