namespace ServidorAPI.Dominio.Servicios.Servidor
{
    public class Lista<Entidad> : List<Entidad>
    {
        public Lista(List<Entidad> items, int totalRegistros, int numeroPagina, int registrosPagina)
        {
            TotalRegistros = totalRegistros;
            RegistrosPagina = registrosPagina;
            PaginaActual = numeroPagina;
            TotalPaginas = (int)Math.Ceiling(totalRegistros / (double)registrosPagina);
            AddRange(items);
        }

        public int PaginaActual { get; set; }
        public int TotalPaginas { get; set; }
        public int RegistrosPagina { get; set; }
        public int TotalRegistros { get; set; }

        public bool ExistePaginaAnterior => PaginaActual > 1;
        public bool ExistePaginaSiguiente => PaginaActual < TotalPaginas;
        public int? NumeroPaginaSiguiente => ExistePaginaSiguiente ? PaginaActual + 1 : null;
        public int? NumeroPaginaAnterior => ExistePaginaAnterior ? PaginaActual - 1 : null;
    }
}