using FluentValidation;
using ServidorAPI.Infraestructura.Filtros.FluentValidator.Utils;
using ServidorAPI.Infraestructura.Objetos.Sadim.Editar;
using ServidorAPI.Infraestructura.Objetos.Sadim.Insertar;

namespace ServidorAPI.Infraestructura.Filtros.FluentValidator.Sadim
{
    public class ValidacionProcesoInsertar : AbstractValidator<ProcesoInsertar>
    {
        public ValidacionProcesoInsertar()
        {
            RuleFor(x => x.Nombre).NombreReq();
            RuleFor(x => x.NombreCorto).NombreReq().When(x => x.Nombre != null);
            RuleFor(x => x.Descripcion).DescripcionReq().When(x => x.NombreCorto != null);
            RuleFor(e => e.Foto!).SetValidator(new ValidacionImagen());
        }
    }

    public class ValidacionProcesoEditar : AbstractValidator<ProcesoEditar>
    {
        public ValidacionProcesoEditar()
        {
            RuleFor(x => x.Nombre).NombreReq().When(x => x.Nombre != null);
            RuleFor(x => x.NombreCorto).NombreReq().When(x => x.NombreCorto != null);
            RuleFor(x => x.Descripcion).DescripcionReq().When(x => x.Descripcion != null);
            RuleFor(e => e.Foto!).SetValidator(new ValidacionImagen());
        }
    }
}