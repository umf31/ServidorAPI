using FluentValidation.Internal;
using System.Linq.Expressions;
using System.Reflection;

namespace ServidorAPI.Infraestructura.Filtros.FluentValidator.Utils
{
    public static class ConfiguracionValidacion
    {
        private static string PropertyNameResolver(Type type, MemberInfo memberInfo, LambdaExpression expression)
        {
            if (expression != null)
            {
                var chain = PropertyChain.FromExpression(expression);
                if (chain.Count > 0)
                {
                    return chain.ToString();
                }
            }

            if (memberInfo != null)
            {
                return memberInfo.Name;
            }

            if (type != null)
            { }

            return null!;
        }

        public static string ModificarNombrePropiedad(Type type, MemberInfo memberInfo, LambdaExpression expression)
        {
            return PropertyNameResolver(type, memberInfo, expression).MinusculaPrimeraLetra();
        }

        public static string MinusculaPrimeraLetra(this string value)
        {
            if (string.IsNullOrWhiteSpace(value) || char.IsLower(value, 0))
            {
                return value;
            }

            if (value.Length == 1)
            {
                return value.ToLower();
            }

            if (value == "ContentType")
            {
                return value;
            }
            var chars = char.ToLowerInvariant(value[0]) + value[1..];

            return chars;
        }
    }
}