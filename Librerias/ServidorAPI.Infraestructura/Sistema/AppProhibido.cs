using Microsoft.AspNetCore.Builder;
using Microsoft.AspNetCore.Http;
using ServidorAPI.Infraestructura.Objetos.Servidor.Respuesta;
using ServidorAPI.Persistencia.Informacion;
using System.Net;

namespace ServidorAPI.Infraestructura.Sistema
{
    public static class AppProhibido
    {
        public static IApplicationBuilder UseForbidden(this IApplicationBuilder app)
        {
            app.Use(async (context, next) =>
            {
                await next();
                if (context.Response.StatusCode == (int)HttpStatusCode.Forbidden)
                {
                    context.Response.ContentType = TipoArchivo.ApplicationJson;
                    var detalleRespuesta = new DetalleRespuesta
                    {
                        Resultado = false,
                        Encabezado = Mensaje.Encabezado.StatusCode403,
                        Detalle = Mensaje.Detalle.Prohibido,
                        StatusCode = Mensaje.Excepcion.StatusCode403,
                        TipoRespuesta = Mensaje.TipoRespuesta.Error
                    };

                    await context.Response.WriteAsync(new ExcepcionRespuesta()
                    {
                        Detalles = detalleRespuesta
                    }.ToString());
                }
            });
            return app;
        }
    }
}