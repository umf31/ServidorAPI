using Microsoft.AspNetCore.Authentication.JwtBearer;
using Microsoft.Extensions.DependencyInjection;
using Microsoft.IdentityModel.Tokens;
using ServidorAPI.Dominio.Servicios.Informacion;
using System.Text;

namespace ServidorAPI.Infraestructura.Sistema
{
    public static class ServicioAuthentication
    {
        public static IServiceCollection AddAutentificacion(this IServiceCollection services)
        {
            services.AddAuthentication(options =>
            {
                options.DefaultAuthenticateScheme = JwtBearerDefaults.AuthenticationScheme;
                options.DefaultChallengeScheme = JwtBearerDefaults.AuthenticationScheme;
            }).AddJwtBearer(options =>
            {
                options.TokenValidationParameters = new TokenValidationParameters
                {
                    ValidateIssuer = true,
                    ValidateAudience = true,
                    ValidateLifetime = true,
                    ValidateIssuerSigningKey = true,
                    ValidIssuer = Configuracion.UrlUnidad,
                    ValidAudience = Configuracion.UrlUnidad,
                    IssuerSigningKey = new SymmetricSecurityKey(Encoding.ASCII.GetBytes(Configuracion.LlavePrivada))
                };
            });
            return services;
        }
    }
}