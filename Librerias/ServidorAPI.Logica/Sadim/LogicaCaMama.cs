        public ILogicaCaMama03Unidad<CaMama03Unidad> LogicaCaMama03Unidad => logicaCaMama03Unidad ?? new LogicaCaMama03Unidad<CaMama03Unidad>(mapper, uts, lista, crear);
    }
}
