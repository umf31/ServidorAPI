using AutoMapper;
using ServidorAPI.Dominio.Entidades.Sadim;
using ServidorAPI.Dominio.Interfaces.Logica.Sadim;
using ServidorAPI.Dominio.Interfaces.UnidadTrabajo;
using ServidorAPI.Dominio.Interfaces.Utils;

namespace ServidorAPI.Logica.Sadim
{
    public class LogicaDiabetes : ILogicaDiabetes
    {
        internal readonly ILogicaDm01Unidad<Dm01Unidad> logicaDm01Unidad = null!;
        internal readonly ILogicaDm02Unidad<Dm02Unidad> logicaDm02Unidad = null!;
        internal readonly ILogicaDm04Unidad<Dm04Unidad> logicaDm04Unidad = null!;
        internal readonly ILogicaDm05Unidad<Dm05Unidad> logicaDm05Unidad = null!;
        private readonly IMapper mapper;
        private readonly ISadimUT uts;
        private readonly IPagSadim lista;
        private readonly ICrearIndicador crear;
        public LogicaDiabetes(IMapper _mapper, ISadimUT _uts, IPagSadim _lista, ICrearIndicador _crear)
        {
            mapper = _mapper;
            uts = _uts;
            lista = _lista;
            crear = _crear;
        }
        public ILogicaDm01Unidad<Dm01Unidad> LogicaDm01Unidad => logicaDm01Unidad ?? new LogicaDm01Unidad<Dm01Unidad>(mapper, uts, lista, crear);

        public ILogicaDm02Unidad<Dm02Unidad> LogicaDm02Unidad => logicaDm02Unidad ?? new LogicaDm02Unidad<Dm02Unidad>(mapper, uts, lista, crear);

        public ILogicaDm04Unidad<Dm04Unidad> LogicaDm04Unidad => logicaDm04Unidad ?? new LogicaDm04Unidad<Dm04Unidad>(mapper, uts, lista, crear);

        public ILogicaDm05Unidad<Dm05Unidad> LogicaDm05Unidad => logicaDm05Unidad ?? new LogicaDm05Unidad<Dm05Unidad>(mapper, uts, lista, crear);


    }
}
