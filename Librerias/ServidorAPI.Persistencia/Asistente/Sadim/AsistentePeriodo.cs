using Microsoft.EntityFrameworkCore;
using ServidorAPI.Dominio.Entidades.Sadim;
using ServidorAPI.Dominio.Excepciones;
using ServidorAPI.Dominio.Interfaces.Asistente.Sadim;
using ServidorAPI.Dominio.Servicios.Informacion;
using ServidorAPI.Persistencia.Conectividad.Contexto;
using ServidorAPI.Persistencia.Utils;

namespace ServidorAPI.Persistencia.Asistente.Sadim
{
    public class AsistentePeriodo : ServidorAsistente<Periodos>, IAsistentePeriodo
    {
        public AsistentePeriodo(ServidorContexto db) : base(db)
        { }

        public async Task<Periodos> ObtenerPeriodo(string? mes, string? año)
        {
            var entidad = await entidades.FirstOrDefaultAsync(x => x.Año == año && x.Mes == mes);
            if (entidad != null)
            {
                return entidad;
            }
            else
            {
                throw new NotFound(Mensaje.Detalle.NoEncontrado);
            }
        }

        public async Task<Periodos> ObtenerPorPeriodo(string periodo)
        {
            var entidad = await entidades.FirstOrDefaultAsync(x => x.Periodo == periodo);
            if (entidad != null)
            {
                return entidad;
            }
            else
            {
                throw new NotFound(Mensaje.Detalle.NoEncontrado);
            }
        }

        public async Task<bool> ExistePeriodoPorMesAño(string? Mes, string? Año)
        {
            var existe = await entidades.Where(x => x.Mes == Mes && x.Año == Año).FirstOrDefaultAsync();
            if (existe != null)
            {
                return true;
            }
            else
            {
                return false;
            }
        }
    }
}