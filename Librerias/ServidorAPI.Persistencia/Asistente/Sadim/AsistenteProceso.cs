using Microsoft.EntityFrameworkCore;
using ServidorAPI.Dominio.Entidades.Sadim;
using ServidorAPI.Dominio.Excepciones;
using ServidorAPI.Dominio.Interfaces.Asistente.Sadim;
using ServidorAPI.Dominio.Servicios.Informacion;
using ServidorAPI.Persistencia.Conectividad.Contexto;
using ServidorAPI.Persistencia.Utils;

namespace ServidorAPI.Persistencia.Asistente.Sadim
{
    public class AsistenteProceso : ServidorBase<Proceso>, IAsistenteProceso
    {
        public AsistenteProceso(ServidorContexto db) : base(db)
        { }

        public async Task<bool> ExisteEntidadPorNombreCorto(string? nombreCorto)
        {
            if (nombreCorto != null)
            {
                var existe = await entidades.Where(x => x.NombreCorto.ToLower() == nombreCorto.ToLower() && x.StatusId == 1).FirstOrDefaultAsync();
                if (existe == null)
                {
                    return false;
                }
                else
                {
                    return true;
                }
            }
            return false;
        }
    }
}