using Microsoft.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore.Metadata.Builders;
using ServidorAPI.Dominio.Entidades.Enlace;

namespace ServidorAPI.Persistencia.Conectividad.FluentAPI.Enlace
{
    public class CIP01_PrevenIMSSFluentAPI : IEntityTypeConfiguration<CIP01_PrevenIMSS>
    {
        public void Configure(EntityTypeBuilder<CIP01_PrevenIMSS> entity)
        {
            entity.HasKey(e => new { e.CvePresup, e.Periodo, e.Consultorio, e.Turno, e.Grupo, e.Indicador, e.Matricula });
            entity.ToTable("tbAtencionIntegralPREVENIMSS");

            entity.Property(e => e.CvePresup).HasMaxLength(12).IsUnicode(false);
            entity.Property(e => e.Periodo).HasMaxLength(6).IsUnicode(false);
            entity.Property(e => e.Consultorio).HasMaxLength(4).IsUnicode(false);
            entity.Property(e => e.Grupo).HasMaxLength(2).IsUnicode(false);
            entity.Property(e => e.Indicador).HasMaxLength(2).IsUnicode(false);
            entity.Property(e => e.Matricula).HasMaxLength(12).IsUnicode(false);
            entity.Property(e => e.CobAip).HasColumnType("decimal(10, 2)").HasColumnName("Cob_AIP");
            entity.Property(e => e.DhAip).HasColumnType("numeric(18, 0)").HasColumnName("DhAIP");
            entity.Property(e => e.Medico).HasMaxLength(150).IsUnicode(false);
            entity.Property(e => e.TotalPob).HasColumnType("numeric(18, 0)").HasColumnName("Total_Pob");
        }
    }
}