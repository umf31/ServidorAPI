using Microsoft.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore.Metadata.Builders;
using ServidorAPI.Dominio.Entidades.Enlace;

namespace ServidorAPI.Persistencia.Conectividad.FluentAPI.Enlace
{
    public class IAP01_ProductividadAPIFluentAPI : IEntityTypeConfiguration<IAP01_ProductividadAPI>
    {
        public void Configure(EntityTypeBuilder<IAP01_ProductividadAPI> entity)
        {
            entity.HasKey(e => new { e.CvePresup, e.Periodo, e.Servicio, e.Matricula });
            entity.ToTable("tbAPI_Productividad");

            entity.Property(e => e.CvePresup).HasMaxLength(12).IsUnicode(false);
            entity.Property(e => e.Periodo).HasMaxLength(6).IsUnicode(false);
            entity.Property(e => e.Servicio).HasMaxLength(2).IsUnicode(false);
            entity.Property(e => e.Matricula).HasMaxLength(12).IsUnicode(false);
            entity.Property(e => e.Api).HasColumnName("API");
            entity.Property(e => e.ProductividadEnf).HasColumnType("numeric(10, 2)").HasColumnName("Productividad_Enf");
        }
    }
}