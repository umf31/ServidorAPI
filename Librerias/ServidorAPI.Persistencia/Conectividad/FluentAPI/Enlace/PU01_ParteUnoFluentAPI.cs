using Microsoft.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore.Metadata.Builders;
using ServidorAPI.Dominio.Entidades.Enlace;

namespace ServidorAPI.Persistencia.Conectividad.FluentAPI.Enlace
{
    public class PU01_ParteUnoFluentAPI : IEntityTypeConfiguration<PU01_ParteUno>
    {
        public void Configure(EntityTypeBuilder<PU01_ParteUno> entity)
        {
            entity.HasNoKey();
            entity.ToTable("tb_Parte_Uno_UM");

            entity.Property(e => e.ConsultasDh).HasColumnName("Consultas_Dh");
            entity.Property(e => e.CvePresup).HasMaxLength(12).IsUnicode(false);
            entity.Property(e => e.Delegacion).HasMaxLength(2).IsUnicode(false);
            entity.Property(e => e.HorasTrab).HasMaxLength(12).IsUnicode(false);
            entity.Property(e => e.Matricula).HasMaxLength(12).IsUnicode(false);
            entity.Property(e => e.Medico).HasMaxLength(250).IsUnicode(false);
            entity.Property(e => e.Periodo).HasMaxLength(6).IsUnicode(false);
            entity.Property(e => e.PromHora).HasMaxLength(12).IsUnicode(false);
            entity.Property(e => e.PromIncap).HasMaxLength(12).IsUnicode(false);
            entity.Property(e => e.Servicio).HasMaxLength(2).IsUnicode(false);
        }
    }
}