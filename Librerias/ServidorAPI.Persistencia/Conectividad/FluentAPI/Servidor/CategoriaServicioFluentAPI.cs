using Microsoft.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore.Metadata.Builders;
using ServidorAPI.Dominio.Entidades.Servidor;

namespace ServidorAPI.Persistencia.Conectividad.FluentAPI.Servidor
{
    public class CategoriaServicioFluentAPI : IEntityTypeConfiguration<CategoriaServicio>
    {
        public void Configure(EntityTypeBuilder<CategoriaServicio> builder)
        {
            builder.ToTable("CategoriaServicio", "catalogo");
            builder.HasKey(x => new { x.IdCategoria, x.IdServicio });
            builder.HasOne(x => x.Servicio).WithMany().HasForeignKey(e => e.IdServicio);

            builder.Property(e => e.IdCategoria).HasColumnOrder(0).IsUnicode(false);
            builder.Property(e => e.IdServicio).HasColumnOrder(1).IsUnicode(false);
        }
    }
}