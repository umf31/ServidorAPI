using Microsoft.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore.Metadata.Builders;
using ServidorAPI.Dominio.Entidades.Soporte;

namespace ServidorAPI.Persistencia.Conectividad.FluentAPI.Soporte
{
    public class MensajeriaFluentAPI : IEntityTypeConfiguration<MensajeriaSoporte>
    {
        public void Configure(EntityTypeBuilder<MensajeriaSoporte> builder)
        {
            builder.ToTable("Mensajeria");
            builder.Property(e => e.Id).HasColumnOrder(0);
            builder.Property(e => e.TwilioCuenta).HasColumnOrder(1).IsRequired().IsUnicode(false);
            builder.Property(e => e.TwilioToken).HasColumnOrder(2).IsRequired().IsUnicode(false);
            builder.Property(e => e.TwilioTelefono).HasColumnOrder(3).IsRequired().IsUnicode(false);
        }
    }
}