using Microsoft.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore.Metadata.Builders;
using ServidorAPI.Dominio.Entidades.Soporte;

namespace ServidorAPI.Persistencia.Conectividad.FluentAPI.Soporte
{
    public class ProcesoFluentAPI : IEntityTypeConfiguration<ProcesoSoporte>
    {
        public void Configure(EntityTypeBuilder<ProcesoSoporte> builder)
        {
            builder.ToTable("Procesos");
            builder.HasIndex(e => e.StatusId, "IX_Procesos_StatusId");
            builder.HasOne(d => d.Status).WithMany(p => p.Procesos).HasForeignKey(d => d.StatusId);

            builder.Property(e => e.Id).HasColumnOrder(0);
            builder.Property(e => e.Nombre).HasColumnOrder(1).IsRequired().IsUnicode(false);
            builder.Property(e => e.NombreCorto).HasColumnOrder(3).IsRequired().IsUnicode(false);
            builder.Property(e => e.Descripcion).HasColumnOrder(4).IsRequired().IsUnicode(false);
            builder.Property(e => e.Imagen).HasColumnOrder(5).IsUnicode(false);
            builder.Property(e => e.FechaCreacion).HasColumnOrder(6).HasColumnType("datetime");
            builder.Property(e => e.FechaModificacion).HasColumnOrder(7).HasColumnType("datetime");
            builder.Property(e => e.UsuarioMod).HasColumnOrder(8).IsUnicode(false);
            builder.Property(e => e.StatusId).HasColumnOrder(9).IsRequired();
        }
    }
}