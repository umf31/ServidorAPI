using System;
using Xunit;

namespace ServidorAPI.PruebasUnitarias.Utils
{
    public static class ConfirmarPrueba
    {
        public static void DebeSerIgual(this object actual, object expected)
        {
            Assert.Equal(expected, actual);
        }

        public static void DeberiaSerVerdadero(this bool b)
        {
            Assert.True(b);
        }

        public static void DeberiaSerFalso(this bool b)
        {
            Assert.False(b);
        }
    }

    public class Validacion<ObjetoTransferencia> : MetodoValidacionPruebas<ObjetoTransferencia>
    {
        public Validacion(params Action<Validacion<ObjetoTransferencia>>[] acciones)
        {
            foreach (var accion in acciones)
            {
                accion(this);
            }
        }
    }
}